#3. Sucessor e Antecessor: Lela um número inteiro e mostre na tela o seu antecessor e a seu sucessor.

print('Mostrar número antecessor e sucessor')

num = int(input('Insira o número desejado: '))

antNum = num - 1
sucNum = num + 1

print('O antecessor de', num, 'é:', antNum)
print('O sucessor de', num, 'é:', sucNum)