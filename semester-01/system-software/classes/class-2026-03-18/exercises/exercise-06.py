#6 Reajuste Salarial: Leia o salário de un funcionário e calcule o novo valor com 15% de aumento.

print("Reajuste salarial")

salario = float(input("Informe seu salario atual:"))

calVal = salario * 0.15

valFinal = salario + calVal

print('O seu salário aumentou de', salario, 'para', valFinal, '!')