#4. Dobro e Triplo: Leia um número e mostre o seu dobro e o seu triplo

print("Multiplicador: Dobro e Triplo")

num = float(input("Insira o número desejado"))

numD = num * 2
numT = num * 3

print(num, "multiplicado por seu dobro é:", numD)
print(num, "multiplicado por seu triplo é:", numT)